# Data Types

# A. Primitive Data Types : That store simple values

integer = 20 #int
floating = 3.14 #float
fullName = "Rampravesh Rana" #string
boolean = False #boolean
noneType = None #NoneType
print(type(integer))
print(type(floating))
print(type(fullName))
print(type(boolean))
print(type(noneType))

# B. Non Primitive data types

fruits = ["apple", "banana", "mango"] #list
numbers = (1, 2, 3) #tuple
unique_numbers = {1, 2, 3, 4, 5, 1} #set
person = {"name": "Rampravesh Rana", "age": 20} #dic
print(type(fruits))
print(type(numbers))
print(type(unique_numbers))
print(type(person))