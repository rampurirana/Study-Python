# String Operation

# 1. Concatenation : Joining string with +
firstName = "Rampravesh"
lastName = "Rana"
fullName = firstName + " " + lastName
print(fullName)

# 2. Repetition : Repeat string with *
repeatName = "Yes" * 3
print(repeatName)

# 3. Length of string : len() function
nickName = "Rampuri Rana"
print(len(nickName))