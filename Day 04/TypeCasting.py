# Type Casting is the process of converting one data type into another

# Types of type casting

# 1. Explicit conversion (Type Casting) : Manually convert one data type to another data type using bult in functions like int(), float(), str()
num_float = 5.8
num_int = int(num_float)
print(num_int)

# 2. Imlicit conversion (Type Casting) : Python automatically convert one data type into another data type during an operation without any user intervetion
number_int = 10
number_float = 4.5
result = number_int + number_float
print(result)