# Taking user input using the input() function. By default, the inpur() function takes the input as string.

# 1. Basic input (String) :
name = input("Enter your name : ")
print("Name :", name)

# 2. Integer input :
age = int(input("Enter your age : "))
print("Age :", age)

# 3. Float input :
height = float(input("Enter your height : "))
print("Height :", height)

# Formatted Print
print(f"My name is {name} and i am {age} years old. My height is {height}ft.")