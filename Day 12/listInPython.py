# Accessing elements : Access elements in list using indexing (Starting from 0) or negative indexing
fruits = ["apple", "Banana", "Mango", "Cherry"]
print(fruits[0]) # apple
print(fruits[-2]) # subtract from len of list = 4-2 = Mango